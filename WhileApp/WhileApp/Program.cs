using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace WhileApp
{
    internal class Program
    {
        static void Main(string[] args)
        {
            //1~200까지

            int i = 1;

            while (i <= 200)
            {
                Console.Write(i + " ");
                i++;
            }
        }
    }
}
